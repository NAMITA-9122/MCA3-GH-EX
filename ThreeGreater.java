class ThreeGreater
{
	public static void main(String[] args)
	{
		int a=90,b=80,c=60;
		
		if(a>b && a>c)
			{
				System.out.println("a is Greater "+a);
			}
		else if (b>c && b>a )
			{
				System.out.println("b is Greater "+b);
			}
		else
			{
				System.out.println("c is Greater "+c);
			}
	}
}
